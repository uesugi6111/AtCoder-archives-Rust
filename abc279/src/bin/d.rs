#[proconio::fastout]
fn main() {
    input!(a: f64, b: f64);

    let mut low = 0;
    let mut high = 1000000000000000000;

    let mut cnt = 1000;
    while cnt > 0 {
        let c1 = (low * 2 + high) / 3;
        let c2 = (low + high * 2) / 3;

        if f(c1, a, b) > f(c2, a, b) {
            low = c1;
        } else {
            high = c2;
        }
        cnt -= 1;
    }
    println!(
        "{}",
        f(low, a, b).min(f(high, a, b).min(f((high + low) / 2, a, b)))
    );
}

fn f(n: i64, a: f64, b: f64) -> f64 {
    (n as f64 * b) + a / (n as f64 + 1.0).sqrt()
}
